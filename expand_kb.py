# For each entry, append domain-specific expansion content
count = 0
for entry in entries:
    domain = entry['path'].split('.')[0]
    if domain in EXPANSIONS:
        expansion = EXPANSIONS[domain]['suffix']
        old_text = entry['text']
        # The old text ends with a " (closing quote)
        # We need to append the expansion before the closing quote
        # Find the last occurrence of the old text in the JS
        if old_text in js:
            # Append expansion to the old text
            new_text = old_text + '\\n' + expansion
            # Escape any double quotes in expansion
            expansion_escaped = expansion.replace('"', '\\"')
            new_text = old_text + '\\n' + expansion_escaped
            js = js.replace(old_text, new_text, 1)
            count += 1

