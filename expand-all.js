// Helper: expand a value at a dot-separated path
function setAtPath(obj, path, newVal) {
  const parts = path.split('.');
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    const p = parts[i];
    const match = p.match(/^(.+)\[(\d+)\]$/);
    if (match) {
      cur = cur[match[1]][parseInt(match[2])];
    } else {
      cur = cur[p];
    }
  }
  const last = parts[parts.length - 1];
  const m = last.match(/^(.+)\[(\d+)\]$/);
  if (m) {
    cur[m[1]][parseInt(m[2])] = newVal;
  } else {
    cur[last] = newVal;
  }
}

function getAtPath(obj, path) {
  const parts = path.split('.');
  let cur = obj;
  for (const p of parts) {
    const m = p.match(/^(.+)\[(\d+)\]$/);
    if (m) cur = cur[m[1]][parseInt(m[2])];
    else cur = cur[p];
  }
  return cur;
}

