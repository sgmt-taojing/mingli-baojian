4. **治未病**：中医预防医学理念，强调疾病未发或初起时通过调养避免加重（如观察睡眠、局部温度变化）。
5. **营卫不和**：表里失调的病理机制，表现为恶风、汗出异常等，需用桂枝汤类方剂调和。
