// Helper: find field value in source and append text
function appendToField(fieldValue, addition) {
  // Convert runtime string to source representation (escape newlines and quotes)
  const sourceStr = JSON.stringify(fieldValue).slice(1, -1); // remove surrounding quotes
  if (s.includes(sourceStr)) {
    const newSourceStr = JSON.stringify(fieldValue + addition).slice(1, -1);
    s = s.replace(sourceStr, newSourceStr);
    return true;
  }
  console.log('NOT FOUND: ' + fieldValue.slice(0, 60));
  return false;
}

