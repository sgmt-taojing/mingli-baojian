// 输出：返回今日(节气/假日)的祝贺,没有则null
// JIEQI / MEMORIAL_DAYS 由调用方通过 init() 注入(避免循环依赖)
var _JIEQI = [];
var _MEMORIAL_DAYS = {};
function init(opts) {
  if (opts && opts.JIEQI) _JIEQI = opts.JIEQI;
  if (opts && opts.MEMORIAL_DAYS) _MEMORIAL_DAYS = opts.MEMORIAL_DAYS;
}
function getGreeting(Y, M, D) {
  // 优先节气
  for (var i = 0; i < _JIEQI.length; i++) {
    if (M === _JIEQI[i].m && D === _JIEQI[i].d) {
      var jg = JIEQI_GREETINGS[_JIEQI[i].name];
      if (jg) return { type: '节气', name: _JIEQI[i].name, text: jg.text, quote: jg.quote };
    }
  }
  // 法定假日/重要节日
  for (var k in HOLIDAY_GREETINGS) {
    if (_MEMORIAL_DAYS[M + '-' + D] === k) {
      var hg = HOLIDAY_GREETINGS[k];
      if (hg) return { type: '节日', name: k, text: hg.text, quote: hg.quote };
    }
  }
  return null;
}

module.exports = { getGreeting, init, JIEQI_GREETINGS, HOLIDAY_GREETINGS };
