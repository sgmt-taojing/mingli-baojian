  // 获取功德等级
  getMeritLevel: function(faith, points) {
    const system = this.merit[faith];
    if (!system) return null;
    
    for (let i = system.levels.length - 1; i >= 0; i--) {
      if (points >= system.levels[i].totalPoints) {
        return {
          current: system.levels[i],
          next: i < system.levels.length - 1 ? system.levels[i + 1] : null,
          progress: points,
          progressToNext: i < system.levels.length - 1 ? 
            system.levels[i + 1].totalPoints - points : 0
        };
      }
    }
    
    return {
      current: null,
      next: system.levels[0],
      progress: points,
      progressToNext: system.levels[0].totalPoints - points
    };
  },

