Page({
  data: {
    categories: CATEGORIES,
    currentCategory: 'bazi',
    articles: [],
    hotArticles: [],
    keyword: '',
    expandedId: '',
    loading: false,
    // 分类标签栏滚动位置
    scrollIntoView: '',
    // 来自首页 wiki 参数的初始分类
    initialCategory: ''
  },

  onLoad(options) {
    // 支持首页 wiki / category / knowledge 跳转：?wiki=bazi?type=bazi?category=bazi
    if (options) {
      const key = options.wiki || options.type || options.category
      if (key && key !== 'history' && key !== 'wiki') {
        // 实际分类如 bazi/ziwei/qimen
        const exists = CATEGORIES.some(c => c.key === key)
        if (exists) {
          this.setData({ currentCategory: key, initialCategory: key, scrollIntoView: 'cat-' + key })
        }
      } else if (key === 'wiki') {
        // 仅表示进入百科页面，保留默认分类
        console.log('进入命理百科')
      }
    }
    this.loadArticles()
    this.loadHotArticles()
  },

  onPullDownRefresh() {
    this.loadArticles(() => {
      wx.stopPullDownRefresh()
    })
  },

  // 加载当前分类文章
  loadArticles(cb) {
    const { currentCategory, keyword } = this.data
    this.setData({ loading: true })

    api.get(`/api/knowledge/list?category=${encodeURIComponent(currentCategory)}&keyword=${encodeURIComponent(keyword)}`)
      .then(res => {
        const list = (res && res.data) || []
        this.setData({ articles: list, loading: false })
        cb && cb()
      })
      .catch(() => {
        // API 不可用，使用内置数据
        let list = FALLBACK_DATA[currentCategory] || []
        if (keyword) {
          const kw = keyword.toLowerCase()
          list = list.filter(item =>
            item.title.toLowerCase().includes(kw) ||
            item.summary.toLowerCase().includes(kw)
          )
        }
        this.setData({ articles: list, loading: false })
        cb && cb()
      })
  },

  // 加载热门推荐
  loadHotArticles() {
    api.get('/api/knowledge/hot')
      .then(res => {
        const list = (res && res.data) || []
        this.setData({ hotArticles: list.slice(0, 5) })
      })
      .catch(() => {
        // 从内置数据中取阅读量最高的
        const all = []
        Object.keys(FALLBACK_DATA).forEach(key => {
          FALLBACK_DATA[key].forEach(item => {
            all.push({ ...item, categoryKey: key })
          })
        })
        all.sort((a, b) => b.reads - a.reads)
        this.setData({ hotArticles: all.slice(0, 5) })
      })
  },

  // 切换分类
  onCategoryTap(e) {
    const key = e.currentTarget.dataset.key
    if (key === this.data.currentCategory) return
    this.setData({
      currentCategory: key,
      expandedId: '',
      scrollIntoView: 'cat-' + key
    })
    this.loadArticles()
  },

  // 搜索输入
  onSearchInput(e) {
    this.setData({ keyword: e.detail.value })
  },

  // 搜索确认
  onSearchConfirm() {
    this.setData({ expandedId: '' })
    this.loadArticles()
  },

  // 清除搜索
  onSearchClear() {
    this.setData({ keyword: '', expandedId: '' })
    this.loadArticles()
  },

  // 展开/收起文章
  onArticleTap(e) {
    const id = e.currentTarget.dataset.id
    this.setData({
      expandedId: this.data.expandedId === id ? '' : id
    })
  },

  // 点击热门推荐
  onHotTap(e) {
    const { category, id } = e.currentTarget.dataset
    this.setData({
      currentCategory: category,
      expandedId: id,
      scrollIntoView: 'cat-' + category
    })
    this.loadArticles(() => {
      this.setData({ expandedId: id })
    })
  }
})
