  function getQuotesByCategory(cat) {
    if (!cat) return allQuotes;
    if (categoryMap[cat]) {
      return categoryMap[cat].slice();
    }
    // 模糊匹配
    for (var key in categoryMap) {
      if (key.indexOf(cat) >= 0 || cat.indexOf(key) >= 0) {
        return categoryMap[key].slice();
      }
    }
    return [];
  }

