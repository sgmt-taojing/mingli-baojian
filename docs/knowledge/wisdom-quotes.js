  /**
   * 按日期哈希生成伪随机数
   * @param {string} dateStr 日期字符串，如 "2024-01-15"
   * @returns {number} 0-1 之间的浮点数
   */
  function dateHash(dateStr) {
    var str = dateStr || "";
    var hash = 0;
    for (var i = 0; i < str.length; i++) {
      hash = ((hash << 5) - hash + str.charCodeAt(i)) | 0;
    }
    // 取绝对值，映射到 0-1
    return (Math.abs(hash) % 10000) / 10000;
  }

