// 获取所有经书列表（用于导航）
SCRIPTURE_DATABASE.getList = function() {
  const list = [];
  for (const category of ['buddhist', 'taoist', 'confucian']) {
    const items = this[category] || [];
    const catName = { buddhist: '佛教', taoist: '道教', confucian: '儒家' }[category];
    for (const item of items) {
      list.push({
        id: item.id,
        name: item.name,
        fullName: item.fullName,
        category: catName,
        length: item.length,
        audioDuration: item.audioDuration,
        description: item.description,
        hasPinyin: !!item.sections,
        type: item.type || ''
      });
    }
  }
  return list;
};

// 获取单部经书
SCRIPTURE_DATABASE.getById = function(id) {
  for (const category of ['buddhist', 'taoist', 'confucian']) {
    const found = (this[category] || []).find(s => s.id === id);
    if (found) return found;
  }
  return null;
};

